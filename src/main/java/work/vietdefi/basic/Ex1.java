package work.vietdefi.basic;

public class Ex1 {
    /**
     * Determines if the given number is even.
     *
     * @param input the number to check
     * @return true if the input is even; false if the input is odd
     */
    public boolean isEvenNumber(int input) {
        if(input %2 == 0){
            return true;
        }else {
            return false;
        }
    }
}


